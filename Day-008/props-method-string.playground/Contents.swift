import UIKit

var greeting = "Hello, playground"
print(greeting.count) // 17
print(greeting.hasPrefix("Hello")) // true
print(greeting.hasSuffix("ground")) // true
print(greeting.uppercased()) // HELLO, PLAYGROUND
print(greeting.lowercased()) // hello, playground
print(greeting.sorted()) // [" ", ",", "H", "a", "d", "e", "g", "l", "l", "l", "n", "o", "o", "p", "r", "u", "y"]
